# Store a person’s name in a variable, and then print that person’s name in lowercase, uppercase, and titlecase


name = "John Doe"


def printUpperCase(name):
    print(name.upper())


def printLowerCase(name):
    print(name.lower())


def printTitleCase(name):
    print(name.title())


printUpperCase(name)
printLowerCase(name)
printTitleCase(name)
